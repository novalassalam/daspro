#include <iostream>
using namespace std; // library untuk memperingkas 
// aplikasi increment decrement
int main() {
    // kamus
    int x = 5;
    int y = 3;
    // algortima
    ++x; // ++ : hasil ditambahkan dulu baru di assign ke x
    y--; // nilai y awal baru dikurangi;


    
    // output
    cout << "x++ adalah:" <<x <<endl;
    cout << "y-- adalah:" <<y <<endl;
    return 0;
}